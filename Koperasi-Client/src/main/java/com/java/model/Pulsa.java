package com.java.model;


public class Pulsa {
	private int id_pulsa;
	private String jenis_pulsa;
	private int harga_pulsa;
	private int stock;
	private String deskripsi;
	public int getId_pulsa() {
		return id_pulsa;
	}
	public void setId_pulsa(int id_pulsa) {
		this.id_pulsa = id_pulsa;
	}
	public String getJenis_pulsa() {
		return jenis_pulsa;
	}
	public void setJenis_pulsa(String jenis_pulsa) {
		this.jenis_pulsa = jenis_pulsa;
	}
	public int getHarga_pulsa() {
		return harga_pulsa;
	}
	public void setHarga_pulsa(int harga_pulsa) {
		this.harga_pulsa = harga_pulsa;
	}
	public int getStock() {
		return stock;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
	public String getDeskripsi() {
		return deskripsi;
	}
	public void setDeskripsi(String deskripsi) {
		this.deskripsi = deskripsi;
	}
	
	
	
}
